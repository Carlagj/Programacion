package Ejercicios;

public class Ejercicio_7 {
    public static void main(String[] args) {
        int X, Y;
        X = 8;
        Y = 4;
        double N, M, resta, suma, multiplicacion, division;
        N = 2.3;
        M = 4.5;

         resta = Y - N;
         suma = X + M;
         multiplicacion = X * Y ;
         division = Y / N ;

        System.out.println(Y + "-" + N + "=" + resta);
        System.out.println(X + "+" + M + "=" + suma );
        System.out.println(X + "*" + Y + "=" + multiplicacion);
        System.out.println(Y + "/" + N + "=" + division);
    }

}
